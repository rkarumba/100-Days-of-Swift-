// This dictionary stores the heights of two musicians.
var heights = [
    "Taylor Swift": 1.78,
    "Ed Sheeran": 1.73
]

// This line of code retrieves the height associated with the key "Taylor Swift".
heights["Taylor Swift"]  // Output: 1.78

// This line of code retrieves the height associated with the key "Ed Sheeran".
heights["Ed Sheeran"]  // Output: 1.73

// This line of code reassigns the value associated with the key "Taylor Swift" to 1.75.
heights["Taylor Swift"] = 1.75

// This line of code retrieves the new height associated with the key "Taylor Swift".
heights["Taylor Swift"]  // Output: 1.75
