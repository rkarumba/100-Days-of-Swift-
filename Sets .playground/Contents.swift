// A set is an unordered collection of unique values in Swift.
// Here, we create a set called 'colors' with three elements "red", "green", and "blue".
let colors = Set(["red", "green", "blue"])

// We can also create a set with duplicate values, but the duplicates will be removed and only unique values will be stored.
// Here, we create a set called 'colors2' with four elements "red", "green", "blue", but since sets only store unique values, the duplicates ("red" and "blue") are removed.
let colors2 = Set(["red", "green", "blue", "red", "blue"])

