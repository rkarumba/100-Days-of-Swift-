// A tuple is a collection of values that can be of different types.
// Here, we define a tuple called "name" with two values: "first" and "last", both of type String.
var name = (first: "Taylor", last: "Swift", age: 56)

// We can access the first value in the tuple by using the index number, starting from 0.
name.0

// Alternatively, we can use the name we gave to the first value when defining the tuple to access it.
name.first

name.age 
