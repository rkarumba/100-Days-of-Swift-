// Tuples are useful when you want to group related values together, especially when each value has a distinct name or meaning.
let address = (house: 555, street: "Taylor Swift Avenue", city: "Nashville")

// Sets are useful when you need to store a collection of values, but you don't care about the order or if each value is unique.
let set = Set(["aardvark", "astronaut", "azalea"])

// Arrays are useful when you want to store a collection of values in a specific order and allow for duplicates.
let pythons = ["Eric", "Graham", "John", "Michael", "Terry", "Terry"]
